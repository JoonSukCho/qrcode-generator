import Head from 'next/head';
import { useState } from 'react';
import { Box, Card, Container, Grid, Stack, Tab, Tabs } from '@mui/material';
import { QRCodeProvider } from '@/components/Provider/QRcodeProvider';
import QRCodeDownload from '@/components/QRCodeDownload';
import QRCodeSettings from '@/components/QRCodeSettings';

export default function Home() {
  const [currentTab, setCurrentTab] = useState(0);

  const onChangeTab = (e: React.SyntheticEvent, newValue: number) => {
    setCurrentTab(newValue);
  };

  return (
    <>
      <Head>
        <title>QR Code Generator</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <QRCodeProvider>
        <Box
          sx={{
            backgroundColor: '#f3f7fa',
          }}
        >
          <Container
            maxWidth="md"
            sx={{
              minHeight: '100vh',
              padding: 4,
            }}
          >
            <Grid
              container
              columnSpacing={2}
              direction={{ xs: 'column', sm: 'row' }}
            >
              <Grid item xs={7}>
                <QRCodeDownload />
              </Grid>

              <Grid item xs={5}>
                <Card
                  variant="outlined"
                  sx={{
                    padding: 2,
                    paddingTop: 1,
                  }}
                >
                  <Tabs
                    value={currentTab}
                    onChange={onChangeTab}
                    sx={{ paddingTop: 0 }}
                  >
                    <Tab label="설정" />
                    <Tab label="스타일" />
                  </Tabs>

                  {/* 설정 탭 */}
                  {currentTab === 0 && <QRCodeSettings />}

                  {/* 스타일 탭 */}
                  {currentTab === 1 && (
                    <Stack spacing={2} sx={{ paddingTop: 2 }}>
                      준비중
                    </Stack>
                  )}
                </Card>
              </Grid>
            </Grid>
          </Container>
        </Box>
      </QRCodeProvider>
    </>
  );
}
